function mouseOn(d) {
  tip.show(d);
  d3
    .select(this)
    .transition()
    .duration(100);
}

function mouseOff(d) {
  tip.hide(d);
  d3
    .select(this)
    .transition()
    .duration(100);
}

function onFlag() {
  is_flag = 1;
  render();
}

function onGeo() {
  is_flag = 0;
  cur_view = "geo";
  render();
}

function onFamily() {
  is_flag = 0;
  cur_view = "family";
  render();
}
